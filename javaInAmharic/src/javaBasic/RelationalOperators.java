package javaBasic;

public class RelationalOperators {

	public static void main(String[] args) {
		// Relational operators are ==, >, <, >=, <=
		int a=100;
		int b=200;
		System.out.println("is " + a +" = "+ b + "? " + (a==b) );
		System.out.println("is " + a +" > "+ b + "? " + (a>b) );
		System.out.println("is " + a +" <= "+ b + "? " + (a<=b) );
		// try by yourself

	}

}
